package com.swingy.models;

/**
 * Position
 */
public class Position {
	private int _x;
	private int _y;

	public Position(int level) {
	}

	public void setX(int x) {
		this._x = x;
	}

	public void setY(int y) {
		this._y = y;
	}

	public int getX() {
		return this._x;
	}

	public int getY() {
		return this._y;
	}
}
